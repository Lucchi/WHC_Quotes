function displayQuote() {
    //create an index of a random number 
    //convert it into between 0 to length of quotes[]
    let index = Math.floor(Math.random() * quotes.length);

    //display the quote of that index
    let div = document.querySelector('#quote');
    let quote = `<div class="card">
<img src="icons/favicon.png">
<p>${quotes[index]}</p>
<img src="icons/favicon.png">
</div>
`;
    let base = document.querySelector('#info');
    let info = `<div class="card">
<p>We currently have ${quotes.length} quotes in our database. This is the quote number ${index + 1}.</p>
</div>
`;
    let footer = document.querySelector('#footer');
    let msg = '<p><b>last modified:</b> ' + document.lastModified + '</p>';
    div.innerHTML = quote;
    base.innerHTML = info;
    footer.innerHTML = msg;
}